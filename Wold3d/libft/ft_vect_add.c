#include "libft.h"

t_vector	ft_vect_add(t_vector a, t_vector b)
{
	t_vector vt;

	vt.x = a.x + b.x;
	vt.y = a.y + b.y;
	vt.z = a.z + b.z;
	return (vt);
}
