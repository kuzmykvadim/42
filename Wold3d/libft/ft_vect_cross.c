#include "libft.h"

t_vector	ft_vect_cross(t_vector *a, t_vector *b)
{
	t_vector	vt;

	vt.x = a->y * b->z - a->z * b->y;
	vt.y = a->z * b->x - a->x * b->z;
	vt.z = a->x * b->y - a->y * b->x;
	return (vt);
}
