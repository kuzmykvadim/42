#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int neg;
	int cn;

	i = 0;
	neg = 0;
	cn = 0;
	while ((str[i] == ' ') || (str[i] == '\n') || (str[i] == '\t')
		|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		cn = cn * 10 + (str[i] - '0');
		i++;
	}
	if (neg == 1)
		return (-cn);
	else
		return (cn);
}
