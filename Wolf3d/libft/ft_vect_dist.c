#include "libft.h"

double		ft_vect_dist(t_vector *a, t_vector *b)
{
	double	dist;

	dist = sqrt((a->x - b->x) * (a->x - b->x) + \
			(a->y - b->y) * (a->y - b->y) + (a->z - b->z) * (a->z - b->z));
	return (dist);
}
