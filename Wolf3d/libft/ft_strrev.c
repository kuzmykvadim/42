#include "libft.h"

char	*ft_strrev(char *str)
{
	char	*s;
	int		i;
	int		k;

	k = 0;
	i = ft_strlen(str);
	s = (char *)malloc(i + 1);
	while (i >= 0)
	{
		s[k] = str[i - 1];
		i--;
		k++;
	}
	s[k - 1] = '\0';
	return (s);
}
