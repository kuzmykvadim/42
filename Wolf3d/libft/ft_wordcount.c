#include "libft.h"

int	ft_wordcount(char const *s, char c)
{
	int a;
	int b;

	a = 0;
	b = 0;
	if (s[0] == '\0')
		return (0);
	while (s[a])
	{
		while (s[a] == c)
		{
			if ((s[a + 1] != c && s[a + 1]) || (s[a] != c && s[a + 1]))
				b++;
			a++;
		}
		if (s[a])
			a++;
	}
	if (s[0] != c)
		b = b + 1;
	return (b);
}
